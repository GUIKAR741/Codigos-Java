    @Override
    public void update() {
        int tipoTemp="ºC".equals(model.getTipoTemperatura())?0:1;
        float temperatura;
        setTxtCidade(model.getNome());
        setTxtUf(model.getUf());
        setTipoTemperatura(tipoTemp);
        setTipoClima("sol".equals(model.getClima())?0:"chuva".equals(model.getClima())?1:2);
        if(tipoTemp==0)
            temperatura=model.getTemperatura();
        else
            temperatura= (float) ((model.getTemperatura()-32)/1.8);
        setTxtTemp(String.valueOf(temperatura));
    }
