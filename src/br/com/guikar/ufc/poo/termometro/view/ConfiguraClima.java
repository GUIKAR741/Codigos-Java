    public float getTemperatura(){
        try{
            return Float.parseFloat(txtTemp.getText());
        }catch(Exception e){
            return 0f;
        }
    }
    public void setTxtTemp(String txtTemp) {
        DecimalFormat df = new DecimalFormat("0.##");
        this.txtTemp.setText(df.format(Double.parseDouble(txtTemp)));
    }
    public int getTipoTemperatura(){
        return radioC.isSelected()?0:1;
    }
    public void setTipoTemperatura(int tTemp) {
        if(tTemp==0){
            radioC.setSelected(true);
            radioF.setSelected(false);
        }else{
            radioC.setSelected(false);
            radioF.setSelected(true);
        }
    }
    public int getTipoClima(){
        return radioSol.isSelected()?0:radioChuva.isSelected()?1:2;
    }
    public void setTipoClima(int tClima){
        switch (tClima) {
            case 0:
                radioSol.setSelected(true);
                radioChuva.setSelected(false);
                radioNublado.setSelected(false);
                break;
            case 1:
                radioSol.setSelected(false);
                radioChuva.setSelected(true);
                radioNublado.setSelected(false);
                break;
            case 2:
                radioSol.setSelected(false);
                radioChuva.setSelected(false);
                radioNublado.setSelected(true);
                break;
        }
    }
